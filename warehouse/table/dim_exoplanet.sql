CREATE TABLE IF NOT EXISTS dim_exoplanet(
        planet_key BIGSERIAL,
        target_name TEXT,
        target_class TEXT,
        spatial_frame_type TEXT,
        instrument_host_name TEXT,
        instrument_name TEXT,
        measurement_type TEXT,
        processing_level INT,
        creation_date TIMESTAMP,
        modification_date TIMESTAMP,
        release_date TIMESTAMP,
        service_title TEXT,
        publisher TEXT,
        bib_reference TEXT,
        target_region TEXT,
        species TEXT,
        detection_type TEXT,
        publication_status TEXT,
        discovered INT,
        remarks TEXT,
        other_web TEXT,
        mass_detection_type TEXT,
        radius_detection_type TEXT,
        alt_target_name TEXT,
        star_name TEXT,
        is_current BOOLEAN,
        current_as_of DATE,
        date_expired DATE,
        external_link TEXT,
        PRIMARY KEY(planet_key)
);